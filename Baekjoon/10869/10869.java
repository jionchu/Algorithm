import java.util.Scanner;
public class Main {
	public static void main(String[] ar){
		Scanner keyboard = new Scanner(System.in);
		int A = keyboard.nextInt();
		int B = keyboard.nextInt();
		System.out.println(A+B);
		System.out.println(A-B);
		System.out.println(A*B);
		System.out.println(A/B);
		System.out.println(A%B);
	}
}
